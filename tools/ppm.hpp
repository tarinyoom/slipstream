inline void write_ppm(const char*            path,
                      std::span<const float> density,
                      int                    nx,
                      int                    ny,
                      float                  vmax  = 1.0f,
                      int                    scale = 1)
{
    FILE* f = std::fopen(path, "wb");
    if (!f) return;
    std::fprintf(f, "P6\n%d %d\n255\n", nx * scale, ny * scale);
    for (int y = ny - 1; y >= 0; --y) {
        for (int sy = 0; sy < scale; ++sy) {
            for (int x = 0; x < nx; ++x) {
                float v = density[y * nx + x] / vmax;
                v = std::clamp(v, 0.0f, 1.0f);
                int idx = (int)(v * 255.0f);
                for (int sx = 0; sx < scale; ++sx)
                    std::fwrite(INFERNO_LUT[idx], 1, 3, f);
            }
        }
    }
    std::fclose(f);
}

